    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed
        String ime = txtIme.getText();
        String prezime = txtPrezime.getText();
        String JMBG = txtJMBG.getText();
        String ziroRacun = txtZiroRacuna.getText();
        double iznos = Double.parseDouble(txtIznos.getText());
        Banka b = (Banka) cmbBanka.getSelectedItem();
        
        if(ime.isEmpty() || prezime.isEmpty() || JMBG.isEmpty() || ziroRacun.isEmpty() || txtIznos.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Sva polja moraju biti popunjena");
            return;
        }
        
        String brojPlatnogPrometa = String.valueOf(b.getJedinstveniBrojPlatPrometa());
        if(!ziroRacun.startsWith(brojPlatnogPrometa)){
            JOptionPane.showMessageDialog(this, "Ziro racuna mora da pocinje kao broj platnog prometa banke");
            return;
        }
        
      
            KlijentskiZahtev kz = new KlijentskiZahtev(Konstante.VRATI_ZIRORACUN, ziroRacun);
            KomunikacijaSaServerom.getInstance().posaljiZahtev(kz);
            
            boolean signal = (boolean) KomunikacijaSaServerom.getInstance().primiOdgovor().getOdgovor();
            if(!signal){
                JOptionPane.showMessageDialog(this, "Ziro racun vec postoji");
                return;
            }
            
            KlijentskiZahtev kz1 = new KlijentskiZahtev(Konstante.VRATI_JMBG, JMBG);
            KomunikacijaSaServerom.getInstance().posaljiZahtev(kz1);
            
            boolean signal1 = (boolean) KomunikacijaSaServerom.getInstance().primiOdgovor().getOdgovor();
            if(!signal1){
                JOptionPane.showMessageDialog(this, "JMBG vec postoji");
                return;
            }
            
            
        
        z.setIme(ime);
        z.setPrezime(prezime);
        z.setJMBG(JMBG);
        z.setZiroRacun(ziroRacun);
        z.setIznos(iznos);
        z.setBanka(b);
        
        
        this.dispose();
    }//GEN-LAST:event_btnDodajActionPerformed

