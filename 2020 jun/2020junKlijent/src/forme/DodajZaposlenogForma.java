    private void srediBanku() {
        KlijentskiZahtev kz = new KlijentskiZahtev(Konstante.VRATI_BANKE, null);
        KomunikacijaSaServerom.getInstance().posaljiZahtev(kz);
        cmbBanka.removeAllItems();
        ArrayList<Banka> listaB = (ArrayList<Banka>) KomunikacijaSaServerom.getInstance().primiOdgovor().getOdgovor();
        
        for (Banka banka : listaB) {
            cmbBanka.addItem(banka);
        }
    }
