    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed
        ModelTabeleZaposleni mtz = (ModelTabeleZaposleni) tblZaposleni.getModel();
        ArrayList<Zaposleni> listaZap = mtz.getListaZap();
        if(listaZap.isEmpty()){
            JOptionPane.showMessageDialog(this, "Mora imati bar jednog zaposlenog");
            return;
        }
        int brojZap = listaZap.size();
        for (Zaposleni zaposleni : listaZap) {
            zaposleni.getKompanija().setBrojZaposlenih(brojZap);
        }
        
        KlijentskiZahtev kz = new KlijentskiZahtev(Konstante.SACUVAJ_KOMPANIJU, listaZap);
        KomunikacijaSaServerom.getInstance().posaljiZahtev(kz);
        
        boolean signal = false;
        signal = (boolean) KomunikacijaSaServerom.getInstance().primiOdgovor().getOdgovor();
        
        if(signal){
            JOptionPane.showMessageDialog(this, "Uspesno sacuvano");
            resetujFormu();
            mtz.resetuj();
            return;
        }else{
            JOptionPane.showMessageDialog(this, "NEUSPESNO sacuvano");
            resetujFormu();
            mtz.resetuj();
            return;
        }
        
        
    }//GEN-LAST:event_btnSacuvajActionPerformed

