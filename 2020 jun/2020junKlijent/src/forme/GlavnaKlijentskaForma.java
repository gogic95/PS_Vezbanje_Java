    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed
       
            if(txtNaziv.getText().isEmpty() || txtMaticniBroj.getText().isEmpty() /*|| txtDatumEvidencije.getText().isEmpty()*/){
                JOptionPane.showMessageDialog(this, "Prvo popunite podatke kompanije");
                return;
            }
            //SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy HH:mm:ss");
            //Date datum = sdf.parse(txtDatumEvidencije.getText());
            String maticni = txtMaticniBroj.getText();
            KlijentskiZahtev kz = new KlijentskiZahtev(Konstante.VRATI_MATICNI, maticni);
            KomunikacijaSaServerom.getInstance().posaljiZahtev(kz);
            
            boolean signal = (boolean) KomunikacijaSaServerom.getInstance().primiOdgovor().getOdgovor();
            if(!signal){
                JOptionPane.showMessageDialog(this, "Maticni broj vec postoji");
                return;
            }
            
            
            
            Kompanija komp = new Kompanija(0l, txtNaziv.getText(), (String) cmbTipKompanija.getSelectedItem(), rbnDa.isSelected(), txtMaticniBroj.getText(), new Date(), 0, korisnik);
            txtDatumEvidencije.setEnabled(false);
            txtMaticniBroj.setEnabled(false);
            txtNaziv.setEnabled(false);
            cmbTipKompanija.setEnabled(false);
            rbnDa.setEnabled(false);
            rbnNe.setEnabled(false);
            
            Zaposleni z = new Zaposleni();
            z.setKompanija(komp);
            DodajZaposlenogForma dzf = new DodajZaposlenogForma(this, true, z);
            dzf.setVisible(true);
            
            ModelTabeleZaposleni mtz = (ModelTabeleZaposleni) tblZaposleni.getModel();
            mtz.dodajUTabelu(z);
            
            
            
            
        
    }//GEN-LAST:event_btnDodajActionPerformed

