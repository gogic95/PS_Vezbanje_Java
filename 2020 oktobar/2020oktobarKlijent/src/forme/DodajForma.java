    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed
        if(txtSemestar.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "popunite sva polja");
            return;
        }
        
        int semestar = Integer.parseInt(txtSemestar.getText());
        if(semestar > 8 || semestar < 1){
            JOptionPane.showMessageDialog(this, "semestar mora biti izmedju 1 i 8");
            return;
        }
        
        pp.setNastavnik((Nastavnik) cmbNastavnik.getSelectedItem());
        pp.setPredmet((Predmet) cmbPredmet.getSelectedItem());
        pp.setStudijskiProgram((String) cmbSmer.getSelectedItem());
        pp.setSemestar(semestar);
        
        this.dispose();
        
    }//GEN-LAST:event_btnDodajActionPerformed

