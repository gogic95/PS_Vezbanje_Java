    private void srediSmerove() {
        cmbSmer.removeAllItems();
        cmbSmer.addItem("ISIT");
        cmbSmer.addItem("MEN");
        cmbSmer.addItem("OM");
        cmbSmer.addItem("UK");
    }

    private void srediNastavnike() {
        cmbNastavnik.removeAllItems();
        KlijentskiZahtev kz = new KlijentskiZahtev(Konstante.VRATI_NASTAVNIKE, null);
        Komunikacija.getInstance().posaljiZahtev(kz);
        
        ArrayList<Nastavnik> lista = (ArrayList<Nastavnik>) Komunikacija.getInstance().primiOdgovor().getOdgovor();
        for (Nastavnik nastavnik : lista) {
            cmbNastavnik.addItem(nastavnik);
        }
    }

    private void srediPredmete() {
        cmbPredmet.removeAllItems();
        KlijentskiZahtev kz = new KlijentskiZahtev(Konstante.VRATI_PREDMETE, null);
        Komunikacija.getInstance().posaljiZahtev(kz);
        
        ArrayList<Predmet> lista = (ArrayList<Predmet>) Komunikacija.getInstance().primiOdgovor().getOdgovor();
        for (Predmet nastavnik : lista) {
            cmbPredmet.addItem(nastavnik);
        }
    }
