    private void btnSacuvajGazdinstvoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajGazdinstvoActionPerformed
         ModelTabeleGazdinstva mtg = (ModelTabeleGazdinstva) tblGazdinstvo.getModel();
         ArrayList<Stado> listaStada = mtg.getListaStada();
         if(listaStada.isEmpty()){
             JOptionPane.showMessageDialog(this, "Mora imati bar jedno stado");
             return;
         }
         SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        try {
            g.setDatumRegistracije(sdf.parse(txtDatum.getText()));
        } catch (ParseException ex) {
            
            JOptionPane.showMessageDialog(this, "Nije uspeo datum");
            return;
            
        }
         g.setGazdinstvoID(Integer.parseInt(lblGazdinstvoID.getText()));
         g.setNaziv(txtNaziv.getText());
         g.setPoljoprivrednik(poljoprivrednik);
         g.setUkupnoSubvencija(Double.parseDouble(txtUkupnoSubvencija.getText()));
         
         for (Stado stado : listaStada) {
            stado.setGazdinstvo(g);
        }
         
         KlijentskiZahtev kz = new KlijentskiZahtev(Konstante.UPDATE_GAZDINSTVO, g);
         KomunikacijaSaServerom.getInstance().posaljiZahtev(kz);
         boolean signal1 = (boolean) KomunikacijaSaServerom.getInstance().primiOdgovor().getOdgovor();
         if(signal1){
             JOptionPane.showMessageDialog(this, "Uspesno izmenjeno gazdinstvo");
         }else{
             JOptionPane.showMessageDialog(this, "NESUSPESNO IZMENJENO GAZDINSTVO");
         }
         
         KlijentskiZahtev kz1 = new KlijentskiZahtev(Konstante.SACUVAJ_STADA, listaStada);
        KomunikacijaSaServerom.getInstance().posaljiZahtev(kz1);
        boolean signal3 = (boolean) KomunikacijaSaServerom.getInstance().primiOdgovor().getOdgovor();
        if(signal3){
            JOptionPane.showMessageDialog(this, "Uspesno sacuvana stada");
            mtg.resetuj();
        }else{
            JOptionPane.showMessageDialog(this, "NEUSPESNO SACUVANA STADA");
            return;
        }
         
         
    }//GEN-LAST:event_btnSacuvajGazdinstvoActionPerformed

