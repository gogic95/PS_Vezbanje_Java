    private void btnKreirajGazdinstvoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKreirajGazdinstvoActionPerformed
        KlijentskiZahtev kz = new KlijentskiZahtev(Konstante.VRATI_MAX_ID, null);
        KomunikacijaSaServerom.getInstance().posaljiZahtev(kz);
        
        int id = (int) KomunikacijaSaServerom.getInstance().primiOdgovor().getOdgovor();
       
        lblGazdinstvoID.setText(String.valueOf(id));
        
        g = new Gazdinstvo();
        g.setGazdinstvoID(id);
        kz.setOperacija(Konstante.NAPRAVI_GAZDINSTVO);
        kz.setParametar(g);
        KomunikacijaSaServerom.getInstance().posaljiZahtev(kz);
        
        boolean signal = (boolean) KomunikacijaSaServerom.getInstance().primiOdgovor().getOdgovor();
        if(signal){
            JOptionPane.showMessageDialog(this, "Uspesno kreirano");
        }else{
            JOptionPane.showMessageDialog(this, "Neuspesno kreirano");
            return;
        }
        
        
    }//GEN-LAST:event_btnKreirajGazdinstvoActionPerformed

    private void btnDodajStadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajStadoActionPerformed
        ModelTabeleGazdinstva mtg = (ModelTabeleGazdinstva) tblGazdinstvo.getModel();
        ArrayList <Stado> listaS = mtg.getListaStada();
        int id = 0;
        for (Stado stado : listaS) {
            if(stado.getStadoID() > id){
                id = stado.getStadoID() + 1;
            }
        }
        Stado s = new Stado();
        s.setGazdinstvo(g);
        s.setStadoID(id);
        StadoForma sf = new StadoForma(this, true,s);
        sf.setVisible(true);
        for (Stado stado : listaS) {
            if(stado.getZivotinja().equals(s.getZivotinja())){
                JOptionPane.showMessageDialog(this, "Vec postoji ta zivotinja");
                return;
            }
        }
        mtg.dodajUlistu(s);
        txtUkupnoSubvencija.setText(String.valueOf(mtg.vratiUkupnuSubvenciju()));
    }//GEN-LAST:event_btnDodajStadoActionPerformed

